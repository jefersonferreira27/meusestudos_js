// Função Math para exibir o maior número
let maior = Math.max(6,12,28,55,8);
console.log(maior);

// Função Math para exibir o menor número
let menor = Math.min(6,12,28,55,8);
console.log(menor);

// Função Math para arredondar 
let arredondar = Math.round(6.87569);
console.log(arredondar);

// Função Math para arredondar pra cima
let arredondarParaCima = Math.ceil(6.87569);
console.log(arredondarParaCima);

