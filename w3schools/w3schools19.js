// Conjuntos em JS
// É uma coleção de valores únicos 

// Como criar um conjunto
/* - Passando um Array para new set()
   - Crie um novo conjunto e use add() para adicionar valores
   - Crie um novo conjunto e use add() para adicionar variáveis
*/

// Exemplo
// Criar uma set
const letters = new set();

// Adicionar valores a set
letters.add('a');
letters.add('b');
letters.add('c');

// Criar variáveis
const a = 'a';
const b = 'b';
const c = 'c';

// Adicionar variavéis a set
letters.add(a);
letters.add(b);
letters.add(c);
