// Objetos em Javascript
// Representação da vida real
// Um objeto possui propriedades
// Os valores são escritos como pares nome:valor(nome e valor separados por dois pontos)
// É comum declarar um objeto com a PALAVRA-CHAVE CONST

// Exemplo:
const times = {
  //Os paises são as propriedades e os times são os valores
  espanha: "ATM",
  italia: "Juve",
  franca: "PSG",
};

// Acessar propriedades do objeto
times.espanha()
// Ou
times["espanha"]

